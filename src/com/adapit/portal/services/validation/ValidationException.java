package com.adapit.portal.services.validation;

import java.io.Serializable;


public class ValidationException extends Exception implements Serializable{

	private static final long serialVersionUID = 35760098278757457L;

	public ValidationException() {
		super();
	}

	public ValidationException(String arg0) {
		super(arg0);
	}

	public ValidationException(Throwable arg0) {
		super(arg0);
	}

	public ValidationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
